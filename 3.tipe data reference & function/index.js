// ⁡⁢⁣⁣​‌‌‍Function
// function adalah baris kode yang memudahkan kita untuk mengulang kode yang sama 
// function bisa dianggap seperti variable juga
// tanda kurung setelah nama function bisa diisi dengan "parameter"
// cara memanggilnya adalah dengan menuliskan nama function diikuti dengan tanda kurung dan parameter yang sesuai

function penjumlahan(angka1, angka2) {
    console.log(angka1 + angka2) 
}
penjumlahan(10,20) //10,20 adalah argumen

function pengurangan(angka1, angka2) {
    console.log(angka1 - angka2) 
}
pengurangan(10,20) //10,20 adalah argumen

function perkalian(angka1, angka2) {
    console.log(angka1 * angka2) 
}
perkalian(10,20) //10,20 adalah argumen

function pembagian(angka1, angka2) {
    console.log(angka1 / angka2) 
}
pembagian(10,20) //10,20 adalah argumen

